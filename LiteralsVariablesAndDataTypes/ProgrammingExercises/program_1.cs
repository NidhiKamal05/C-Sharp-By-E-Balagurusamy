using System;

class Variables
{
	public static void Main()
	{
		char ch = 'A' ;
		sbyte sb = 50 ;
		int i = 123456789 ;
		long l = 1234567654321 ;
		bool b = true ;
		decimal d = 0.000000345m ;
		double d_ = 1.23e5 ;
		System.Console.WriteLine("ch: {0}", ch);
		System.Console.WriteLine("sb: {0}", sb);
		System.Console.WriteLine("i: {0}", i);
		System.Console.WriteLine("l: {0}", l);
		System.Console.WriteLine("b: {0}", b);
		System.Console.WriteLine("d: {0}", d);
		System.Console.WriteLine("d_: {0}", d_);
	}
}